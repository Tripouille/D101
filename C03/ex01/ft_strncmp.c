int		ft_strncmp(char *s1, char *s2, unsigned int n)
{
	if ((*s1 || *s2) && (*s1 == *s2 && n && n - 1))
		return (ft_strncmp(++s1, ++s2, --n));
	else
		return (n ? (unsigned char)*s1 - (unsigned char)*s2 : 0);
}
