int		ft_sqrt(int nb)
{
	int		sqrt;

	sqrt = 2;
	if (nb == 1)
		return (1);
	while (nb > 0 && (sqrt * sqrt) > 0 && (sqrt * sqrt) < nb)
		sqrt++;
	return ((nb > 0 && sqrt * sqrt == nb) ? sqrt : 0);
}
