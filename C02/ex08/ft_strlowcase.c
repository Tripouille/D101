char	*ft_strlowcase(char *str)
{
	int		i;

	i = 0;
	if (!str)
		return (0);
	while (str[i])
		str[i] >= 'A' && str[i] <= 'Z' ? str[i++] -= 'A' - 'a' : i++;
	return (str);
}
